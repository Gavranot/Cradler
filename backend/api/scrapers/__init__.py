@router.post("", response_model=ScraperResponse, status_code=status.HTTP_201_CREATED)
async def create_scraper(
    scraper_data: ScraperCreate,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db)
):
    """
    Create new scraper

    For MVP, this creates a scraper with placeholder config.
    In production, this would trigger the Secondary Agent workflow.
    """
    # Extract domain from URL
    try:
        parsed_url = urlparse(scraper_data.target_url)
        target_domain = parsed_url.netloc
    except Exception:
        target_domain = None

    # Create scraper with placeholder configuration
    # In production, this would be generated by Secondary Agent
    placeholder_config = {
        "status": "pending_generation",
        "message": "Scraper configuration will be generated by Secondary Agent",
        "target_url": scraper_data.target_url,
        "created_via": "chat" if scraper_data.chat_session_id else "manual"
    }

    new_scraper = Scraper(
        id=uuid.uuid4(),
        user_id=current_user.id,
        name=scraper_data.name,
        target_url=scraper_data.target_url,
        target_domain=target_domain,
        scraping_config=placeholder_config,
        status="inactive"  # Will be 'active' after Secondary Agent generates code
    )

    db.add(new_scraper)

    # If created from chat, link to chat session
    if scraper_data.chat_session_id:
        result = await db.execute(
            select(ChatSession).where(
                ChatSession.id == scraper_data.chat_session_id,
                ChatSession.user_id == current_user.id
            )
        )
        chat_session = result.scalar_one_or_none()
        if chat_session:
            chat_session.scraper_id = new_scraper.id

    await db.commit()
    await db.refresh(new_scraper)

    return new_scraper


@router.get("", response_model=List[ScraperListResponse])
async def list_scrapers(
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
    status_filter: str = None
):
    """List all scrapers for current user"""
    query = select(Scraper).where(Scraper.user_id == current_user.id)

    if status_filter:
        query = query.where(Scraper.status == status_filter)

    query = query.order_by(Scraper.created_at.desc())

    result = await db.execute(query)
    scrapers = result.scalars().all()

    return scrapers


