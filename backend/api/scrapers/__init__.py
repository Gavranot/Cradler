    scraper = result.scalar_one_or_none()

    if not scraper:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Scraper not found"
        )

    # Get runs
    result = await db.execute(
        select(ScrapingRun)
        .where(ScrapingRun.scraper_id == scraper_id)
        .order_by(ScrapingRun.created_at.desc())
        .limit(limit)
    )
    runs = result.scalars().all()

    return runs


