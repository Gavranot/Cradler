@router.put("/{scraper_id}", response_model=ScraperResponse)
async def update_scraper(
    scraper_id: UUID,
    scraper_update: ScraperUpdate,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db)
):
    """Update scraper configuration"""
    result = await db.execute(
        select(Scraper).where(
            Scraper.id == scraper_id,
            Scraper.user_id == current_user.id
        )
    )
    scraper = result.scalar_one_or_none()

    if not scraper:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Scraper not found"
        )

    # Update fields if provided
    if scraper_update.name is not None:
        scraper.name = scraper_update.name
    if scraper_update.schedule_cron is not None:
        scraper.schedule_cron = scraper_update.schedule_cron
    if scraper_update.status is not None:
        scraper.status = scraper_update.status
    if scraper_update.scraping_config is not None:
        scraper.scraping_config = scraper_update.scraping_config

    scraper.updated_at = datetime.utcnow()

    await db.commit()
    await db.refresh(scraper)

    return scraper


@router.delete("/{scraper_id}", status_code=status.HTTP_204_NO_CONTENT)
async def delete_scraper(
    scraper_id: UUID,
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db)
):
    """Delete scraper and all associated runs"""
    result = await db.execute(
        select(Scraper).where(
            Scraper.id == scraper_id,
            Scraper.user_id == current_user.id
        )
    )
    scraper = result.scalar_one_or_none()

    if not scraper:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Scraper not found"
        )

    # Delete associated runs first
    await db.execute(
        select(ScrapingRun).where(ScrapingRun.scraper_id == scraper_id)
    )

    await db.delete(scraper)
    await db.commit()

    return None


